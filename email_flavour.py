def _daily_rng(today):
    """Same pick all day (a genuine 'of the day'), changes day to day."""
    return random.Random(today.toordinal())


def _run_rng(today, client_name, cid):
    """Varies per audit run, so two audits on the same day feel fresh."""
    return random.Random(f"{today.toordinal()}|{client_name}|{cid}")


def opener_line(today, client_name, cid):
    rng = _run_rng(today, client_name, cid)
    return rng.choice(_OPENERS).format(client=client_name)


def effort_line(today, client_name, cid, duration_str, tokens_used):
    rng = _run_rng(today, client_name, cid)
    tok = f"{tokens_used:,}" if tokens_used else "a handful of"
    return rng.choice(_EFFORT_LINES).format(duration=duration_str, tokens=tok)


def quote_of_the_day(today):
    quote, who = _daily_rng(today).choice(_QUOTES)
    return quote, who


def fun_fact_of_the_day(today):
    """Returns a true fact. Prefers a dated 'on this day' line for today's date,
    else a general fun fact. Never invents a date."""
    key = today.strftime("%m-%d")
    if key in _ON_THIS_DAY:
        year, event = _ON_THIS_DAY[key]
        return f"On this day in {year}, {event}"
    return _daily_rng(today).choice(_GENERAL_FACTS)
