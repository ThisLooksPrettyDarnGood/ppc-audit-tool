"""
email_flavour.py
----------------
The 'human' bits of the audit completion email: a friendly opener, a real
quote of the day, and a true 'on this day' fun fact.

HARD RULE (same as the decks): never confidently wrong. Every quote and every
historical fact in here has been hand-checked. Nothing is generated by GPT at
send time, because a hallucinated 'on this day in 1722...' would be exactly the
kind of confidently-wrong claim the rest of this tool works hard to avoid.

Selection is seeded by the date, so the "quote of the day" is the same for the
whole team on a given day, while the jokey opener varies per audit run.
"""

import random


